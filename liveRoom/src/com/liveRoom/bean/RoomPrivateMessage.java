package com.liveRoom.bean;

import java.util.Date;

/**
 * 房间私聊信息表实体类
 * @author d
 *
 */
public class RoomPrivateMessage {
	private int room_private_message_Id;//消息id
	private int room_private_message_UserSend;//发送人
	private int room_private_message_UserReceive;//接收人
	private Date room_private_message_Date;//发送时间
	private String room_private_message_Content;//发送内容
	private int room_private_message_State;//状态
	private int room_private_message_Room;//房间外键
	public int getRoom_private_message_Id() {
		return room_private_message_Id;
	}
	public void setRoom_private_message_Id(int room_private_message_Id) {
		this.room_private_message_Id = room_private_message_Id;
	}
	public int getRoom_private_message_UserSend() {
		return room_private_message_UserSend;
	}
	public void setRoom_private_message_UserSend(int room_private_message_UserSend) {
		this.room_private_message_UserSend = room_private_message_UserSend;
	}
	public int getRoom_private_message_UserReceive() {
		return room_private_message_UserReceive;
	}
	public void setRoom_private_message_UserReceive(
			int room_private_message_UserReceive) {
		this.room_private_message_UserReceive = room_private_message_UserReceive;
	}
	public Date getRoom_private_message_Date() {
		return room_private_message_Date;
	}
	public void setRoom_private_message_Date(Date room_private_message_Date) {
		this.room_private_message_Date = room_private_message_Date;
	}
	public String getRoom_private_message_Content() {
		return room_private_message_Content;
	}
	public void setRoom_private_message_Content(String room_private_message_Content) {
		this.room_private_message_Content = room_private_message_Content;
	}
	public int getRoom_private_message_State() {
		return room_private_message_State;
	}
	public void setRoom_private_message_State(int room_private_message_State) {
		this.room_private_message_State = room_private_message_State;
	}
	public int getRoom_private_message_Room() {
		return room_private_message_Room;
	}
	public void setRoom_private_message_Room(int room_private_message_Room) {
		this.room_private_message_Room = room_private_message_Room;
	}

}
